package entity;

public class Order {
    private int id;
    private String pelanggan, produk,staff,date;

    public Order(int id, String pelanggan, String produk, String staff, String date) {
        this.id = id;
        this.pelanggan = pelanggan;
        this.produk = produk;
        this.staff = staff;
        this.date = date;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getPelanggan() {
        return pelanggan;
    }

    public void setPelanggan(String pelanggan) {
        this.pelanggan = pelanggan;
    }

    public String getProduk() {
        return produk;
    }

    public void setProduk(String produk) {
        this.produk = produk;
    }

    public String getStaff() {
        return staff;
    }

    public void setStaff(String staff) {
        this.staff = staff;
    }

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }
}
